def lookup_function(addr, bank=0):
    """Look up a function name by address and bank number.

    For addresses below 0x8000, always uses BANK0_FUNCTIONS (bank 0 code space).
    For addresses >= 0x8000, uses BANK1_FUNCTIONS if bank==1, otherwise BANK0_FUNCTIONS.

    Args:
        addr: Integer address to look up.
        bank: Bank number (0 or 1). Default is 0.

    Returns:
        Function name string if found, or None if no function at that address.
    """
    if addr < 0x8000:
        return BANK0_FUNCTIONS.get(addr)
    else:
        if bank == 1:
            return BANK1_FUNCTIONS.get(addr)
        else:
            return BANK0_FUNCTIONS.get(addr)
